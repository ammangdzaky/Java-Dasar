/**

FLOATING POINT NUMBER

- float : 3.4e-038 sampai 3.4e+038
- double : 1.7e-308 sampai 1.7e+308

3.4e-038 = 3.4 x 10^-38 = 0.000000000000000000000000000000000000034

*/

public class FloatingPointNumber {
    public static void main(String[] args) {

    float IniFloat = 21.123f; //khusus untuk float, bisa ditambahkan f/F untuk menandakan dia float
    double IniDouble = 0.123333333333333331;

    System.out.println(IniFloat);
    System.out.println(IniDouble);
    }
}
