// data character/huruf di Java direpresentasikan oleh tipe char.
// untuk membuat data char di java, kita menggunakan tanda petik satu ('')
// char hanya bisa menampung satu huruf

public class TipeDataChar {
    public static void main(String[] args) {

        // char hai = 'HAI'; -> ini eror (bukan satu huruf)
        char d = 'D';
        char z = 'Z';
        char a = 'A';
        char k = 'K';
        char y = 'Y';

//      System.out.println(d); -> artinya print kemudian tambahkan line/enter
        System.out.print(d); // -> print saja (tidak ada enter/line)
        System.out.print(z);
        System.out.print(a);
        System.out.print(k);
        System.out.print(y);
    }
}