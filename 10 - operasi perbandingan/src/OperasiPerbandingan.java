/**
 *
 * operasi perbandingan -> menghasilkan boolean true or false
 *
 *    >
 *    <
 *    >=
 *    <=
 *    ==
 *    !=
 */

public class OperasiPerbandingan {
    public static void main(String[] args) {


        int a = 10;
        int b = 9;
        boolean lebihBesar = a > b;
        System.out.println(lebihBesar);
        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a >= b);
        System.out.println(a <= b);
        System.out.println(a == b);
        System.out.println(a != b);
    }
}